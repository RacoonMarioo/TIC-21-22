package MiCodigo;

public class Punto {
	private double coordenadasX;
	private double coordenadasY;
	public Punto(double coordenadasX, double coordenadasY) {
		this.coordenadasX = coordenadasX;
		this.coordenadasY = coordenadasY;
	}
	/**
	 * @return the coordenadasX
	 */
	public double getCoordenadasX() {
		return coordenadasX;
	}
	/**
	 * @param coordenadasX the coordenadasX to set
	 */
	public void setCoordenadasX(double coordenadasX) {
		this.coordenadasX = coordenadasX;
	}
	/**
	 * @return the coordenadasY
	 */
	public double getCoordenadasY() {
		return coordenadasY;
	}
	/**
	 * @param coordenadasY the coordenadasY to set
	 */
	public void setCoordenadasY(double coordenadasY) {
		this.coordenadasY = coordenadasY;
	}
	
}
